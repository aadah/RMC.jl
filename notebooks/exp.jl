# ╔═╡ 0ebc8da7-12a9-458b-ad55-87fb84f9059f
function plot_samples_1d(result, S; from=-1, to=1, fig_size=(6.9, 4))
	w, h = fig_size
    set_default_plot_size(w * inch, h * inch)
	
	samples = [samp[1] for samp in result.samples]
	Gadfly.with_theme(:dark) do
		p = plot(θ -> S([θ]), from, to, color=[colorant"gold"])
		push!(p, layer(
			x=samples,
			Geom.density,
			color=[colorant"red"],
			linestyle=[:dash],
		))
		return p
	end
end

# ╔═╡ 021eb6ee-9337-49a0-bb33-5400a2c63093
function plot_trajectory(result, S; from=-1, to=1, fig_size=(6.9, 4))
	w, h = fig_size
    set_default_plot_size(w * inch, h * inch)
	
	T = hcat(result.trajectory...)
	Gadfly.with_theme(:dark) do
		p = plot(θ -> S([θ]), from, to, color=[colorant"gold"])
		push!(p, layer(
			x=T[1,:], y=T[2,:], 
			Geom.path,
			linestyle=[:dash],
			color=[colorant"green"],
			Theme(line_width=0.1mm),
		))
		x = [θ[1] for θ in result.samples]
		y = [S(θ) for θ in result.samples]
		push!(p, layer(
			x=x,
			y=y,
			Geom.point,
			color=[colorant"red"],
			Theme(point_shapes=[Shape.diamond]),
		))
		return p
	end
end

