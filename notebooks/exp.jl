# ╔═╡ 6960744b-3fba-4175-b7b9-80943105dff3
begin
	θ(x) = x[1:length(x)-1]
	h(x) = x[end]
end

# ╔═╡ 0af5da2a-9eaf-4fd1-bd34-f7a85b54c8f4
function leapfrog(x, v, a; step_size=1e-2)
	v += 0.5 * step_size * a
	x += step_size * v
	v += 0.5 * step_size * a
	return x, v
end

# ╔═╡ db1007ef-9802-4077-9c9f-dca193c29c39
iscollision(x, S) = h(x) <= S(θ(x))

# ╔═╡ 522ee3e7-6a95-4eef-a727-f717cb4805a2
function reflect(hyperplane, x, v; anneal=1.0)
	n = gradient(hyperplane, x)[1]
	n /= norm(n)
	v -= 2 * dot(n, v) * n
	v *= clamp(anneal, 0, 1) # entropic slow down
	return v
end

# ╔═╡ 9dcac828-331e-4ec6-8013-2f0331b87e45
struct RMCResult
	samples::Vector
	trajectory::Vector
	boosts::Integer
	speeds::Vector
	g::Real
end

# ╔═╡ 7c38faaf-87ae-479e-ae64-c9bf9cd275a9
function getsamples(
	E::Function, D::Integer, num_samples::Integer;
	g::Union{Nothing,Real}=nothing,
	anneal::Real=1.0,
	min_speed::Real=0.0,
	step_size::Real=1e-2,
	speed_buffer::Integer=10,
)
	samples = []
	trajectory = []
	boosts = 0
	speeds = []

	S(θ) = -log(E(θ))
	hyperplane(x) = S(θ(x)) - h(x)
	
	θ0 = randn(D)
	elevation = S(θ0) + rand(Exponential(S(θ0)))
	# elevation = S(θ0)*2
	x = [θ0..., elevation]
	v = randn(D+1)
	running_speed_window = [] # optimize running avg

	g = abs(isnothing(g) ? randn() : g)
	a = [zeros(D)..., -g]
	
	while length(samples) < num_samples
		push!(trajectory, x)
		speed = norm(v)
		push!(speeds, speed)
		
		x, v = leapfrog(x, v, a, step_size=step_size)
		
		if iscollision(x, S)
			sample = θ(x)
			push!(samples, sample)
			
			# set height to be on U so reflection is accurate
			x = [sample..., S(sample)]
			v = reflect(hyperplane, x, v, anneal=anneal)

			# reset speed annealing causes it to fall low enough
			# idea: the speed at collision can be small at parabola peaks, and so reset could happen there. we don't want that, but rather want the speed boost when either elevation variation or speed variation has been slow the last few rounds

			# only do boosting if annealing is happening
			if anneal < 1
				cur_speed = norm(v)
		
				push!(running_speed_window, cur_speed)
				if length(running_speed_window) > speed_buffer
					popfirst!(running_speed_window)
				end
				
				if mean(running_speed_window) < min_speed
					new_speed = abs(randn()) + min_speed
					v *= (new_speed / cur_speed)
					boosts += 1
				end
			end
		end
	end
	
	return RMCResult(
		samples,
		trajectory,
		boosts,
		speeds,
		g,
	)
end

